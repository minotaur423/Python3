def gcd(a, b):
    gcd = 0
    for i in range(max(a, b)):
        if a < b:
            b = b - a
        elif b < a:
            a = a - b
        elif a == b:
            gcd = a
            break
    return gcd

def lcm(c, d):
    lcm = (c * d) / gcd(c, d)
    return int(lcm)

